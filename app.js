function increase()
{
    var value = parseInt(document.getElementsByClassName('qty')[0].value, 10);
    value = isNaN(value) ? 0 : value;
    if(value<10){
        value++;
            document.getElementsByClassName('qty')[0].value = value;
    }
}
function decrease()
{
    var value = parseInt(document.getElementsByClassName('qty')[0].value, 10);
    value = isNaN(value) ? 0 : value;
    if(value>=1){
        value--;
            document.getElementsByClassName('qty')[0].value = value;
    }

}

